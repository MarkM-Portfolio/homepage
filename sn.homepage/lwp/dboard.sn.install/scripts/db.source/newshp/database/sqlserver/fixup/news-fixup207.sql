CREATE VIEW HOMEPAGE.MENTIONS_READERS AS (
	SELECT r.CATEGORY_READER_ID, r.READER_ID, r.CATEGORY_TYPE, r.SOURCE, r.CONTAINER_ID, r.ITEM_ID,
			r.ROLLUP_ENTRY_ID, r.RESOURCE_TYPE, r.CREATION_DATE, r.STORY_ID, r.SOURCE_TYPE,
			r.USE_IN_ROLLUP, r.IS_NETWORK, r.IS_FOLLOWER, r.EVENT_TIME, r.IS_STORY_COMM,
			r.IS_BROADCAST, r.ORGANIZATION_ID, r.ACTOR_UUID, r.ROLLUP_AUTHOR_ID,
			r.IS_VISIBLE, r.MAX_UPDATE_FOR_READER
	FROM HOMEPAGE.HOMEPAGE.NR_RESPONSES_READERS r, HOMEPAGE.NR_STORIES s 
	WHERE r.STORY_ID = s.STORY_ID
	AND s.EVENT_NAME IN('community.wall.notification.mention',
						'community.wall.comment.notification.mention',
						'profiles.status.notification.mention',
						'profiles.wallpost.notification.mention',
						'profiles.status.comment.notification.mention',
						'profiles.wallpost.comment.notification.mention')
);
GO

