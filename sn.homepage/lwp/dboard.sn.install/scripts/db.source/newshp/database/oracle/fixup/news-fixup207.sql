CREATE VIEW HOMEPAGE.MENTIONS_READERS AS (
    SELECT r.CATEGORY_READER_ID, r.READER_ID, r.CATEGORY_TYPE, r.SOURCE, r.CONTAINER_ID,
    	r.ITEM_ID, r.ROLLUP_ENTRY_ID, r.RESOURCE_TYPE, r.CREATION_DATE,
	r.STORY_ID, r.SOURCE_TYPE, r.USE_IN_ROLLUP, r.IS_NETWORK, r.IS_FOLLOWER,
	r.EVENT_TIME, r.IS_STORY_COMM, r.IS_BROADCAST, r.ORGANIZATION_ID,
	r.ACTOR_UUID, r.ROLLUP_AUTHOR_ID, r.IS_VISIBLE, r.MAX_UPDATE_FOR_READER
    FROM HOMEPAGE.NR_RESPONSES_READERS r, HOMEPAGE.NR_STORIES s
    WHERE r.STORY_ID = s.STORY_ID
    AND s.EVENT_NAME IN ('community.wall.notification.mention',
			 'community.wall.comment.notification.mention',
			 'profiles.status.notification.mention',
			 'profiles.wallpost.notification.mention',
			 'profiles.status.comment.notification.mention',
			 'profiles.wallpost.comment.notification.mention')
);

DECLARE 
CURSOR s_cur IS 
    SELECT *
    FROM HOMEPAGE.MENTIONS_READERS;

 TYPE fetch_array IS TABLE OF s_cur%ROWTYPE;
s_array fetch_array;

BEGIN
    OPEN s_cur;
    LOOP
	FETCH s_cur BULK COLLECT INTO s_array LIMIT 1000;

	FORALL i IN 1..s_array.COUNT
	INSERT INTO HOMEPAGE.NR_MENTIONS_READERS VALUES s_array(i);
	COMMIT;
	
	EXIT WHEN s_cur%NOTFOUND;
    END LOOP;
    CLOSE s_cur;
    COMMIT;
END;
/

commit;

